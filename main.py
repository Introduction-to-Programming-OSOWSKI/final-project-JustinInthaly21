def times10(x):
    return x * 10

def firstA(x):
    if x[0] == "a":
        return True
    else:
        return False

def sizer(n):
    if n < 10:
        return "small"
    elif 9 < n < 101:
        return "medium"
    else:
        return "large"

def doubleWord(w):
    return w + w 

def penguin(p):
    return p 

